/**
 *  toUpperCase()
 *  te permite cambiar un texto todo a mayusculas
 */

const prod = "palabra"
console.log(prod.toUpperCase())

/**
 *  toLowerCase()
 *  te permite cambiar un texto todo a minusculas
 */

const prod2 = "PALABRA"
console.log(prod2.toLowerCase())

/**
 *  to.String()
 *  te permite cambiar un tipo de dato cualquiera a un dato tipo string
 */

const cantidad = 200

console.log(cantidad)
console.log(cantidad.toString())