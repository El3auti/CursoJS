
// NO ES IGUAL

const puntos = 1000

if (puntos != 1000) {
    console.log('SI! es diferente!')
} else {
    console.log('No! no es diferente')
}

// COMPARADOR ESTRICTO (TIPO Y VALOR)

if (puntos === 1000) {
    console.log('Si es igual')
} else {
    console.log('No no es igual')
}


// COMPARADOR ESTRICTO (TIPO Y VALOR NO ES IGUAL)

if (puntos !== 1000) {
    console.log('Si es DIFERENTE ESTRICTO')
} else {
    console.log('no no es igual')
}